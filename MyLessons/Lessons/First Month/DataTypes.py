#Aşağıda Python-da ən çox istifadə olunan verilən tiplərini qısa izahlarla təqdim edirəm:

# 1. Tam ədəd (int)
# Tam ədədləri ifadə edir. Məsələn: 1, -5, 42.
tam_ede = 10

# 2. Ondalık ədəd (float)
# Ondalık ədədləri ifadə edir. Məsələn: 3.14, -0.001, 2.0.
ondalik_ede = 3.14

# 3. Sətir (str)
# Mətni ifadə edir. Tək və ya cüt tirnaq içində yazılır. Məsələn: "Salam", 'Dünya'.
metn = "Salam, dünya!"

# 4. Boolean (bool)
# Mantıksal dəyərləri ifadə edir. True və ya False dəyərlərini alır.
dogru = True
yanlis = False

# 5. Siyahı (list)
# Sıralı və dəyişdirilə bilən bir verilən strukturu. Məsələn: [1, 2, 3], ["alma", "armut"].
siyahi = ["alma", "banan", "çiyələk"]

# 6. Tuple
# Sıralı lakin dəyişdirilə bilməyən bir verilən strukturu. Məsələn: (1, 2, 3), ("A", "B").
tuple_melumat = (10.0, 20.0)

# 7. Set
# Təkil (təkrarlanmayan) elementlərdən ibarət bir verilən strukturu. Sırasızdır. Məsələn: {1, 2, 3}.
set_melumat = {1, 2, 3, 4, 5}

# 8. Lüğət (dict)
# Açar-dəyər cütlərindən ibarət bir verilən strukturu. Məsələn: {"ad": "Ali", "yaş": 22}.
lugat = {"ad": "Ali", "yaş": 22}
